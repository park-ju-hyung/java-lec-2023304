/*
 * 이름 : 이종석
 * 사는곳 : 서울 금천구 
 * 직장 : 서울 금천구
 * "안녕하세요"
 */
public class LiterEx {
	
	public static void main(String[] args) {
		// 리터럴은 \n, \t, \ 등 문자열을 출력/저장 하고 싶을때 사용하는 규칙
		System.out.print("이름 : 이종석\n");
		System.out.print("사는곳 : 서울 금천구\n");
		System.out.print("직장 : 서울 금천구\n");
		System.out.print("\"안녕하세요\"");
		
	}
}
